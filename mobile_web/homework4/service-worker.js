function setBG(val){
    if(val == "lightblue"){
      document.body.style.backgroundImage= "url('lightblue.jpg')"; 
      document.body.style.backgroundSize="cover";    
    }
    if(val == "lightgold"){
      document.body.style.backgroundImage= "url('lightgold.jpg')";
      document.body.style.backgroundSize="cover";    
    }
}
